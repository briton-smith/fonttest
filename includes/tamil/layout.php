	<p style="font-size: 24px;">எண்ணங்கள் யாவும் மனதில் ஒட்டிக் கொண்டு உத்வேகத் தீயை மூட்டிக் கொண்டிருக்கும்</p>
	<p>&nbsp;</p>
	
	<div class="multicolumn3">
		
		<p>அவரின் எண்ணங்கள் பலவும் என் வாழ்வின் மீதான எண்ணவோட்டத்தில் ஆதிக்கம் செலுத்தியது என்பதை இங்கு சொல்லியே ஆகவேண்டும்..
		ஒரு தனிமனிதனின் எண்ணவோட்டத்தின் நிலையில் தான் அவனின் வாழ்வின் வெளிச்சமே தங்கியிருக்கிறது என்ற கருத்தின் ஆணித்தரமாக இருந்தவர் அவர். ஆசிரியராக, கவிஞராக, எழுத்தாளராக, செயற்பாட்டாளராக அவர் வாழ்வில் கொண்ட பாத்திரங்கள் பல. ஆனால், இவை எல்லாவற்றிற்கும் மேலாக ஒரு தலை சிறந்த மனிதனாக உருவாகி உலகத்தைக் கவர்ந்த அவரின் தன்மை மிக முக்கியமானது.
		தனது படைப்புகளின் மூலம், மனங்களை கொள்ளை கொள்கின்ற ஆற்றல் அவருக்கு வாய்த்திருந்தது.</p>
		
