	<p style="font-size: 60px;">மாயா என்ஜெலோ: அணைந்தும் அணையாத ஒளி</p>
	
	<p style="font-size: 24px;">நிறத்தின் முந்திய பதிவுகளில் ஒரு ஆளுமை பற்றி அடிக்கடி சொல்லியிருப்பேன். நான் நண்பர்களோடு சம்பாஷிக்கின்ற போதும் கூட இந்த ஆளுமை பற்றிச் சொல்லுகின்ற தேவை உண்டாகும். நான் நேசிக்கின்ற, மதிக்கின்ற மிகப்பெரும் ஆளுமை – மாயா என்ஜெலோ. இன்று இவ்வுலகத்தை விட்டு, உயிர் துறந்தார் என்ற செய்தி, சோகத்தை கொண்டு தந்தது.</p>
