	<div class="multicolumn3">
		<p>ஒருவனின் வெற்றி என்பது தன்னைப் பற்றிய புரிதலிலேயே தொடங்குகிறது. அந்தப் புரிதல் என்பது, தன்னைத் தானே காதல் கொள்வதில்தான் தொடக்கம் காண்கின்றது என்பதை தனது படைப்புகளின் வாயிலாகச் சொல்லி இந்த உலகின் கவனத்தை கவர்ந்த சாதனைப் பெண்மணிதான் மாயா என்ஜெலோ. 
		இவரின் எழுத்துக்களை வாசிக்கின்ற நிலையில், நான் பல விடயங்களைப் பற்றிய உத்வேகத்தை என் உணர்வுகளுக்குள் எப்போதும் இருப்பதாய் சேமித்து வைத்திருக்கிறேன். 
		நான் என்பது, என் உடலோடு முடிவதல்ல, நான் அதை எப்படி எடுத்து நடக்கின்றேன். நான் என்மீது கொண்டுள்ள செல்வாக்கு என்ன, என் விழிகளுக்குப் பின்னால் எப்போதும் உற்சாகமாய்த் துடிக்கும் அதீத ஆர்வம், அறிவின் நிலைகள் யாவை? என்பதை எப்போதும் ஆய்ந்தறிந்து நடந்து கொள்ள வேண்டும் என்கின்ற எண்ண நிலை அவரின் படைப்புகளை நுகர்ந்ததால் பெற்ற முதன்மையான உத்வேகம் என்பேன்.</p>
